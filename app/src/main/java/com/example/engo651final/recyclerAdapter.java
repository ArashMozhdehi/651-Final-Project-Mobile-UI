public class recyclerAdapter extends RecyclerView.Adapter<recyclerAdapter.MyViewHolder> {
    private ArrayList<FeedbackItem> feedbacksList;
    private Context mContext;


    public recyclerAdapter(ArrayList<FeedbackItem> feedbacksList, Context mContext) {
        this.feedbacksList = feedbacksList;
        this.mContext = mContext;
    }

    public recyclerAdapter(ArrayList<FeedbackItem> feedbacksList) {
        this.feedbacksList = feedbacksList;
    }

    public class MyViewHolder extends RecyclerView.ViewHolder {
        TextView tv_feedback;
        RatingBar rb_feedbacks;
        CardView cv_feedbacks;
        RelativeLayout rl_feedbacks;

        public MyViewHolder(View itemView) {
            super(itemView);
            tv_feedback = itemView.findViewById(R.id.tv_feedback);
            rb_feedbacks = itemView.findViewById(R.id.rb_feedbacks);
            cv_feedbacks = itemView.findViewById(R.id.cv_feedbacks);
            rl_feedbacks = itemView.findViewById(R.id.rl_feedbacks);
        }
    }

    @NonNull
    @Override
    public recyclerAdapter.MyViewHolder onCreateViewHolder(@NonNull ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext()).inflate(R.layout.feedback_items, parent, false);
//        MyViewHolder holder = new MyViewHolder(view);
        return new MyViewHolder(view);
    }

    @Override
    public void onBindViewHolder(@NonNull recyclerAdapter.MyViewHolder holder, int position) {
        Log.d("New call", "onBindViewHolder: Called");
        holder.tv_feedback.setText(feedbacksList.get(position).comment);
        holder.rb_feedbacks.setRating(feedbacksList.get(position).rate);
    }

    @Override
    public int getItemCount() {
        return feedbacksList.size();
    }

}
